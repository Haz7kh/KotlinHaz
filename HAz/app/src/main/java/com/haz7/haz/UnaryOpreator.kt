package com.haz7.haz

fun main(args: Array<String>) {
    var num:Double = 7.6
    val isCheck:Boolean=true
    println("+num = "+ +num)
    println("+num = "+ -num)
    println("+num = "+ ++num)
    println("+num = "+ --num)
    println("!isCheck = "+ !isCheck)
    println("______________________________________________________")

    var result:Double = 4.7
    println("result++ = " + result++)
    println("result = " + result)
    println("result++ = " + ++result)
    println("result = " + result)




}