package com.haz7.haz

fun main(args: Array<String>) {
    val num1:Double=8.4
    val num2:Double=4.2
    var result:Double=0.0

    result=num1 + num2
    println("the ADD = $result")
    result=num1 - num2
    println("the Sub = $result")
    result=num1 * num2
    println("the Mult = $result")
    result=num1 / num2
    println("the Div = $result")
    result=num1 % num2
    println("the reminder = $result")
}